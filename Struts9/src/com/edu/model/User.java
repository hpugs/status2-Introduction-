package com.edu.model;

public class User {
	private int name;
	private int password;
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public int getPassword() {
		return password;
	}
	public void setPassword(int password) {
		this.password = password;
	}
}
